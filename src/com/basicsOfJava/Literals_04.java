//literals are the constant value which can be assigned to the variable
package com.basicsOfJava;
public class Literals_04 {
    public static void main(String[] args) {
        byte age=34;// here 34 is an integer literal
        int age2= 94; // ""
        short age3= 15; // ""
        long ageDino= 555463653569L; //L or l is necessary at the end of long data type
        char ch='A'; // A is a Character literal here(character should be written in single quote)
        float f1= 5.6f; // 5.6f is a float literal here(f is compulsory)
        double d1= 4.66d; // double literal(d or D is optional)
        boolean a= true; // true is a boolean literal
        String str= "Aditya"; // string literal
        System.out.println(age);
        System.out.println(str);
    }
}
